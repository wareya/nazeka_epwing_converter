lazy_static! {
    // group 1: reading
    // group 2: pitch
    // group 3: spellings
    // group 4: PoS etc.
    static ref SECOND_HEADING_REGEX:Regex = Regex::new(r"([^\[]+)(\[[0-9]\])? ?(„Äê.+„Äë)?(.+)?").unwrap();

    static ref EXAMPLE_REGEX:Regex = Regex::new(r"(„Äå[^„Äå]*‚Äï[^„Äå]*„Äç)").unwrap();
}

fn daijirin_strip_second_heading(definition: &mut Vec<String>)
{
    if !definition.is_empty()
    {
        let second_heading = SECOND_HEADING_REGEX.captures(&definition[0]).unwrap();
        definition[0] = format!("Info: {}{}",
                                second_heading.get(2).map_or("", |m| m.as_str()),
                                second_heading.get(4).map_or("", |m| m.as_str()));
        if definition[0].as_str() == "Info: "
        {
            definition.remove(0);
        }
    }
}

fn daijirin_strip_examples(definition: &mut Vec<String>)
{
    let mut i = 0;
    while i < definition.len()
    {
        let mut new = definition[i].clone();
        for capture in EXAMPLE_REGEX.captures_iter(&definition[i])
        {
            new = new.replace(capture.get(0).map_or("", |m| m.as_str()), "");
        }
        definition[i] = new;
        i += 1;
    }
}

pub(crate) fn convert_daijirin(entry: &EpwingEntry) -> Option<JsonEntry>
{
    if let (Some(mut definition), Some((reading, spellings))) = (daijirin_body_converter(&entry.text), daijirin_heading_converter(&entry.heading, true))
    {
        daijirin_strip_second_heading(&mut definition);
        Some(JsonEntry { r: reading, s: spellings, l: definition })
    } else {
        None
    }
}

